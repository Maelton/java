public class OperadoresRelacionais {
    
    public static void main(String[] args) {
        int a = 5;
        double b = 8.1;

        //Menor que (<)
        System.out.print("a<b : "); System.out.println(a<b); 
        System.out.println();
        
        //Maior que (>)
        System.out.print("a>b : "); System.out.println(a>b); 
        System.out.println();

        //Menor igual a (<=)
        System.out.print("a<=b : "); System.out.println(a<=b); 
        System.out.println();

        //Maior igual a (>=)
        System.out.print("a>=b : "); System.out.println(a>=b); 
        System.out.println();

        //Igual a (==)
        System.out.print("a==b : "); System.out.println(a==b); 
        System.out.println();

        //Diferente de (!=)
        System.out.print("a!=b : "); System.out.println(a!=b); 
        System.out.println();
    }
}
